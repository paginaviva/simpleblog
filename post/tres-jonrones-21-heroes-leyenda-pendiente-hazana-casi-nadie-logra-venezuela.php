<h2>Lo que hace único al "tres a la calle" en Venezuela</h2>

<p><strong>Escasez y contexto.</strong> Veintidós noches en casi 80 temporadas subrayan lo extraordinario de la gesta. A diferencia de otros circuitos, los parques venezolanos, el clima, el uso intensivo de bullpens y la paridad competitiva tienden a <strong>romper la rutina del bateador</strong>: hay menos turnos "regalados", más cambios de brazo y una cultura táctica que ataca de inmediato al cañón encendido. Por eso sorprenden más los renglones con <strong>7 u 8 impulsadas</strong> en estas historias (Taylor 1954, Harrelson 1964, Darwin 1972, Lane 2001).</p>

<p><strong>Los escenarios de enero.</strong> Tres de los 22 juegos ocurrieron en <strong>semifinales</strong>: <strong>Richard Hidalgo</strong> (2007), <strong>Robert Pérez</strong> (2009) y <strong>Mario Lissón</strong> (2014). En postemporada, donde la <strong>calidad del pitcheo</strong> sube y el <strong>scouting</strong> es más específico, encadenar tres swings de vuelta entera requiere una sintonía fina que trasciende la "racha".</p>

<p><strong>La irrupción criolla.</strong> Desde <strong>Tomás Pérez</strong> (2001) se abrió un caudal de protagonistas locales que cambió la demografía del listado. Allí conviven <strong>slugger de proyección MLB</strong> (Cabrera, Hidalgo, Lissón) con <strong>bateadores de oficio</strong> (Castellano, Teodoro Martínez, Robert Pérez), y esa diversidad recuerda que no existe un solo molde para lograrlo: <strong>poder bruto</strong>, <strong>elevación controlada</strong> o <strong>lectura quirúrgica de la zona</strong> pueden conducir al mismo resultado.</p>

<p><strong>La Guaira, fábrica reciente.</strong> El siglo XXI dejó una curiosa concentración: <strong>cuatro</strong> noches guairistas en poco más de una década (Van Slyke, Herrera, Cabrera, Castro). No es casual: en ese tramo, <strong>Macuto y el Universitario</strong> fueron escenarios donde el lineup litoral supo crear <strong>tráfico temprano</strong> y obligar a los rivales a lanzar en zona a sus bates de medio, condición ideal para que aparezca el tercer cohete.</p>

<h2>El caso único de los <strong>cuatro</strong> jonrones (fuera de la LVBP)</h2>

<p>El beisbol profesional venezolano registra <strong>una sola</strong> jornada de <strong>cuatro cuadrangulares</strong> por un jugador: el <strong>8 de enero de 1956</strong>, el estadounidense <strong>Russell Rac</strong> (Pastora) lo logró frente a <strong>Petroleros de Cabimas</strong> en el estadio <strong>Alejandro Borges</strong> de Maracaibo, dentro de la <strong>Liga Occidental de Béisbol Profesional</strong>. Esa liga coexistió históricamente con el circuito central; por tanto, <strong>no pertenece a la LVBP</strong>. La <strong>LVBP</strong> —también en sus recuentos recientes— reconoce a Rac como el único con 4 HR "en la pelota rentada nacional", marcando el matiz clave: <strong>en la LVBP, la cifra tope sigue siendo 3</strong>. El "casi" más reciente fue el de <strong>Wilson García</strong> en 2025, que falló sus últimos dos turnos camino al hipotético cuarto batazo.</p>

<h2>Wilson García: contexto actual de una gesta que agranda la lista</h2>

<p>El 14/11/2025, <strong>Bravos 7–3 Cardenales</strong> en Barquisimeto fue el marco del ingreso de <strong>Wilson García</strong> al club. Ambidiestro, descargó <strong>dos jonrones a la derecha</strong> y <strong>uno a la zurda</strong> entre el 2.º y 5.º inning (dos frente al zurdo <strong>Cleiverth Pérez</strong> y uno ante <strong>Robinson Hernández</strong>), impulsó cuatro, anotó tres y, con el último bambinazo, se montó en el <strong>liderato de HR de la temporada (7)</strong>. La actuación tiene dos ribetes históricos: (1) es el <strong>primer 3-HR</strong> en la historia de la franquicia <strong>Bravos/Petroleros/Pastora</strong>, y (2) según los recuentos especializados, sería el <strong>primer juego de tres jonrones en la LVBP con vuelacercas desde ambos lados del plato</strong> en una misma noche.</p>

<h2>Curiosidades y datos finos de la cofradía</h2>

<ul>
<li><strong>21 peloteros, 22 noches.</strong> El club lo integran <strong>21 nombres</strong>; el único repetido es <strong>Robert Pérez</strong>, autor de dos partidos de 3 HR (uno en ronda regular y otro en semifinales).</li>
<li><strong>Tres en enero.</strong> <strong>Hidalgo (2007)</strong>, <strong>Pérez (2009)</strong> y <strong>Lissón (2014)</strong> lo hicieron en <strong>semifinales</strong>, subiendo el grado de dificultad por la calidad del pitcheo y la preparación de los cuerpos técnicos.</li>
<li><strong>La trilogía litoral (2012–2013).</strong> En 14 meses, <strong>La Guaira</strong> celebró tres actuaciones (Van Slyke, Herrera y <strong>Alex Cabrera</strong>), esta última en el siempre exigente <strong>Luis Aparicio "El Grande"</strong>.</li>
<li><strong>Primera vez por franquicia Bravos.</strong> La ruta <strong>Petroleros (1991) → Pastora → Bravos (2007)</strong> no había registrado una hazaña de 3 HR <strong>hasta 2025</strong>, cuando <strong>García</strong> estrenó el casillero.</li>
<li><strong>Temporada 2001-02, bisagra.</strong> <strong>Tomás Pérez</strong>, <strong>Pedro Castellano</strong> y <strong>Jason Lane</strong> firmaron la única zafra con <strong>tres</strong> juegos de 3 HR, abriendo la etapa moderna y el "despertar criollo".</li>
<li><strong>Interliga en los 60.</strong> El listado oficial incorpora el caso <strong>Jim Pendleton (1960)</strong> frente a <strong>Rapiños</strong>, en duelo <strong>LVBP–Liga Occidental</strong>. Es uno de los escasos partidos interligas que figuran en registros de hazañas.</li>
<li><strong>Ambidiestros con algo más.</strong> Hubo switch-hitters en la lista (p. ej., <strong>Tomás Pérez</strong>), pero <strong>Wilson García</strong> queda señalado como el <strong>primero</strong> en lograr <strong>tres HR alternando ambos lados</strong> del plato <strong>en el mismo juego</strong>.</li>
<li><strong>Parques y épocas.</strong> De los estadios antiguos (Universitario, Canónico) a sedes modernas, la constante es la <strong>calidad del turno</strong>: muchos de estos juegos suman <strong>7 u 8 CI</strong>, señal de rallies y de que el bateador "vio grande" toda la noche.</li>
</ul>

<h2>Cómo leer estas hazañas en clave LVBP</h2>

<p>Estas 22 noches funcionan como <strong>hitos narrativos</strong> de la liga: condensan épocas (la de importados dominantes, la modernidad criolla), exhiben <strong>identidades de franquicia</strong> (Caracas y Magallanes con múltiples protagonistas; La Guaira como fábrica reciente) y confirman que el poder en la LVBP suele ser <strong>"útil"</strong>: aparece a menudo en <strong>momentos de palanca</strong>, no como fuegos artificiales inconsecuentes. También iluminan el peso de la <strong>postemporada</strong>: que tres integrantes del club hayan aparecido en semifinales sugiere que el "tres a la calle" no es incompatible con el pitcheo de élite, siempre que el bateador sostenga <strong>plan de turno</strong> y reciba <strong>pitcheos en zona</strong>.</p>

<p>En lo simbólico, la proeza de <strong>Wilson García</strong> robustece la <strong>memoria de Bravos</strong> —una franquicia que se reconoce joven frente a los colosos históricos— y recuerda que la LVBP se sigue reescribiendo con páginas grandes. En lo histórico, el horizonte del <strong>cuarto cuadrangular</strong> sigue siendo un <strong>santo grial</strong> en este circuito: solo existe documentación de un <strong>4-HR</strong> en la <strong>pelota profesional venezolana</strong> (Russell Rac, 1956, <strong>Liga Occidental</strong>). La frontera, por tanto, permanece invicta en la LVBP.</p>

<h2>Cierre: un club breve que huele a enero</h2>

<p>Del <strong>Taylor de 1954</strong> al <strong>García de 2025</strong>, la LVBP ha visto apenas <strong>22</strong> jornadas de tres jonrones. Son pocas, sí, pero cada una cuenta mucho: hablan de <strong>calidad</strong>, de <strong>contexto competitivo</strong> y de una liga capaz de abrazar la épica sin perder su pulso táctico. Y si el "cuarto" batazo jamás ha caído en el circuito central, ese vacío también tiene valor: mantiene viva la búsqueda. A fin de cuentas, la <strong>historia del beisbol venezolano</strong> se ha escrito —y se seguirá escribiendo— con noches como estas, en las que un bate, por nueve entradas, parece más grande que el estadio.</p>
